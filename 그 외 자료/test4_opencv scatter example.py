#cv2.line(img, (400, 405), (400, 405), (255,185, 203), 5)
#cv2.line(img, (400, 410), (400, 410), (255,185, 203), 5)

cv2.line(img, (0.01, -0.08), (0.01, -0.08), (255,185, 203), 5)

cv2.imshow('img',img)
cv2.waitKey()
cv2.destroyAllWindows()
